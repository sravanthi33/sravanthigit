class SwitchCase
{
public static void main(String a[])
{
int i=4;
switch(i)
{
   case 0:
       System.out.println("zero");
       break;
   case 1:
       System.out.println("one");
       break;
   case 4:
       System.out.println("four");
       break;
   default:
       System.out.println("invalid number");
}
}
}